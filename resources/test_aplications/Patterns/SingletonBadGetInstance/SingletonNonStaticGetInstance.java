public class SingletonNonStaticGetInstance {

	private static SingletonNonStaticGetInstance instance;
	
	private SingletonNonStaticGetInstance() {
		
	}
	
	private SingletonNonStaticGetInstance getInstance() {
		if (instance == null) {
			instance = new SingletonNonStaticGetInstance();
		}
		return instance;
	}
	
}
